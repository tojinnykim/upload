# 학생 관리 프로그램

# 1. 학생들 저장할 리스트
students = []

# 2. 새로운 학생 추가 (딕셔너리 + 튜플 학번)
s1 = {
    "학번": (1001,),   # 튜플로 학번
    "이름": "홍길동",
    "나이": 20,
    "과목": {"국어": 90, "영어": 85, "수학": 95}
}
students.append(s1)

s2 = {
    "학번": (1002,),
    "이름": "이순신",
    "나이": 25,
    "과목": {"국어": 100, "영어": 88, "수학": 92}
}
students.append(s2)

# 3. 중복 없는 과목 관리 (set 활용)
subjects = {"국어", "영어", "수학"}

# 4. 데이터 확인
print("학생 명단:", students)
print("모든 과목:", subjects)

# 5. 홍길동의 영어 점수를 80으로 수정
students[0]["과목"]["영어"] = 80
print("홍길동 영어 점수:", students[0]["과목"]["영어"])